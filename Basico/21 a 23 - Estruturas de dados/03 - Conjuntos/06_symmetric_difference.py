# Aula 23 - Unindo dados com o metodo .symmetric_difference (Conjuntos) 
# Symmetric_Difference junta todos os DIFERENTES que não estão na intersecção(os iguais entre os dois conjuntos)

conjunto_a = {1,2,3}
conjunto_b = {2,3,4}

print(conjunto_a.symmetric_difference(conjunto_b)) # o resto que não está na intersecção dos conjuntos é = {1,4}