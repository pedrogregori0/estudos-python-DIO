# Aula 23 - Unindo dados com o metodo .difference (Conjuntos) 
# Difference junta apenas os DIFERENTES dos dois conjuntos 

conjunto_a = {1,2,3}
conjunto_b = {2,3,4}

print(conjunto_a.difference(conjunto_b)) # o que tem no conjunto A que é diferente do B = {1} 
print(conjunto_b.difference(conjunto_a)) # o que tem no conjunto B que é diferente do A = {4} 