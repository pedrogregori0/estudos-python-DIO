# Aula 22 - (Tuplas)
# Acessando diretamente as informações da Tupla

# Vai funcionar igual a lista (Acesso atraves de um paramêtro)

frutas = ("Strawberry", "Apple", "Grappes",) #Tupla com conteúdo
frutas[0]
frutas[2] #Acessa o conteudo pelo indice passado

#Indices negativos

frutas[-1] # O parametro -1 sempre pega o ultimo item da Tupla
frutas[-3] # Pega o valor referente a -3 contando pra trás da Tupla 