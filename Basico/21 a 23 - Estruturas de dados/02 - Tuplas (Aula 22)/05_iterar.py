# Aula 22 - iterar (Tuplas) 
# utilizando o For podemos passar a tupla como parametro, fazendo ele acessar cada item dentro dela

equipes = ("Redbull","Audi/Sauber","McLaren","Ferrari","Aston Martin",)
pilotos = ("M. Verstappen","G. Bortoleto","L. Norris", "C. Leclerc", "F. Alonso",)

for equipe in equipes:
    print(equipe)
for piloto in pilotos:
    print(piloto)
     