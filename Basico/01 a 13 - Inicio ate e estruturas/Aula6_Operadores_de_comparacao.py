#Aula 6
#Operadores de comparação (==, !=, >, <, >=, <=)

saldo = 100
saque = 25

print(saque == saldo) #Comparativo de igual (==)
print(saque != saldo) #Comparativo de diferente (!=)
print(saque >= saldo) #Comparativo de maior ou igual (> para maior ou >=)
print(saque <= saldo) #Comparativo de menor ou igual (< para menor ou <=)
