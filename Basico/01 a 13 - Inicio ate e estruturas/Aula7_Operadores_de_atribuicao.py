#Operadores de Atribuição (=, +=, -=, *=, /=, //=)

Saldo = 500 #Atribuindo o valor para variavel, atraves do "=" (lembrando que ele ira substituir o valor sempre que for definido embaixo)
print(Saldo) #Resultado (500)

Saldo += 500 #Atribuindo e somando o valor para variavel, atraves do "+=" 
print(Saldo) #Resultado (500 + 500 = 1000)

Saldo -= 250 #Atribuindo e subtraindo o valor para variavel, atraves do "-="
print(Saldo) #Resultado (1000 - 250 = 750)

Saldo *= 2 #Atribuindo e multiplicando o valor para variavel, atraves do "*="
print(Saldo) #Resultado (750 * 2 = 1500)

Saldo /= 2 #Atribuindo e dividindo o valor para variavel, atraves do "/="
print(Saldo) #Resultado (1500 / 2 = 750.0 (sempre que dividir ele ira dar em float o resultado))

Saldo //= 2 #Atribuindo e dividindo o valor para variavel, atraves do "//="
print(Saldo) #Resultado (750 / 2 = 350.0 (ira aparecer somente a parte inteira da divisão))
