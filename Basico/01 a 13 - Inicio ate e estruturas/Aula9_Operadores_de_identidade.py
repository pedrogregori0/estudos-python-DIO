#Operadores de identidade (is, is not), só operadores utilizados para comparar se os objetos utilizados ocupam o mesmo espaço na memória

saldo = 1000
limite = 500

print(saldo is limite) #is é o operador de identidade (resultado é Falso, eles não ocupam o mesmo lugar de memória)
print(saldo is not limite)

